package install

import (
	"testing"

	"kubestash.dev/kubestash/apis/addons/fuzzer"
	"kubestash.dev/kubestash/apis/addons/v1alpha1"

	clientsetscheme "k8s.io/client-go/kubernetes/scheme"
	crdfuzz "kmodules.xyz/crd-schema-fuzz"
)

func TestPruneTypes(t *testing.T) {
	Install(clientsetscheme.Scheme)

	// v1alpha1
	if crd := (v1alpha1.Addon{}).CustomResourceDefinition(); crd.V1 != nil {
		crdfuzz.SchemaFuzzTestForV1CRD(t, clientsetscheme.Scheme, crd.V1, fuzzer.Funcs)
	}
}
